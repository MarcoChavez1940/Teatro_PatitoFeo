    private void chooseDirectionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseDirectionButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        
        int option = fileChooser.showOpenDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            String directory = fileChooser.getSelectedFile().getAbsolutePath();
            directionTextField.setText(directory);
        }
    }//GEN-LAST:event_chooseDirectionButtonActionPerformed

    private void cleanFields(){
        ((JTextField)startDateChooser.getDateEditor().getUiComponent()).setText("");
        ((JTextField)endingDateChooser.getDateEditor().getUiComponent()).setText("");
        directionTextField.setText("");
    }
    
    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        
        boolean isFieldEmpty = ((JTextField)startDateChooser.getDateEditor().getUiComponent()).getText().isEmpty()
                || ((JTextField)endingDateChooser.getDateEditor().getUiComponent()).getText().isEmpty()
                || directionTextField.getText().isEmpty();
                    
        if(!isFieldEmpty){
            try {
                printCalendarController.printCalendar(
                        Date.valueOf(printCalendarController.formatDate(startDateChooser)),
                        Date.valueOf(printCalendarController.formatDate(endingDateChooser)),
                        directionTextField.getText());
                JOptionPane.showMessageDialog(this, "Calendario guardado");
                cleanFields();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(PrintCalendarView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (DocumentException ex) {
                Logger.getLogger(PrintCalendarView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Campos vacios");
        }
        
    }//GEN-LAST:event_generateButtonActionPerformed

