    private void addShowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addShowButtonActionPerformed
        boolean saveSuccessful = addShowController.addShow(
                nameShowTextField.getText(),
                descriptionShowTextField.getText(),
                Time.valueOf(hourSpinner.getValue().toString() + ":" + minutesSpinner.getValue().toString() + ":00"),
                priceShowTextField.getText(),
                actorsShowTextField.getText(),
                publicityShowTextField.getText(),
                phonePersonalShowTextField.getText(),
                phoneAlternativeShowTextField.getText(),
                emailShowTextField.getText()
        );
        if(saveSuccessful == true) {
            cleanFields();
        }
    }//GEN-LAST:event_addShowButtonActionPerformed

