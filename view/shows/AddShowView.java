    private void priceShowTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_priceShowTextFieldKeyTyped
        char typed = evt.getKeyChar();
        if(!(Character.isDigit(typed))
           || (typed == KeyEvent.VK_BACK_SPACE)
           || (typed == KeyEvent.VK_DELETE)
           ){
            evt.consume();
        }
    }//GEN-LAST:event_priceShowTextFieldKeyTyped

