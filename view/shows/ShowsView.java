    private void seeResponsibleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeResponsibleButtonActionPerformed
        int showSelected = tableShows.getSelectedRow();
            
        if(showSelected != -1){
            int idShow = (int) tableShows.getValueAt(showSelected, 0);
            showsController.seeResponsibleShow(idShow);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione primero una obra");
        }
    }//GEN-LAST:event_seeResponsibleButtonActionPerformed

    private void cancelShowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelShowButtonActionPerformed
        int showSelected = tableShows.getSelectedRow();
            
        if(showSelected != -1){
            int dialogResult = JOptionPane.showConfirmDialog(this, "Cancelar el show, implica cancelar las funciones hechas\n Â¿desea continuar la cancelacion?");

            if(dialogResult == JOptionPane.YES_OPTION){               
                int idShow = (int) tableShows.getValueAt(showSelected, 0);
                showsController.cancelFunctionRelated(idShow);
                JOptionPane.showMessageDialog(this, "Show cancelado");                
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione primero una obra");
        }          
    }//GEN-LAST:event_cancelShowButtonActionPerformed

    private void watchFunctionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_watchFunctionsActionPerformed
        int showSelected = tableShows.getSelectedRow();
            
        if(showSelected != -1){
            int idShow = (int) tableShows.getValueAt(showSelected, 0);
            showsController.openFuncionRelatedView(idShow);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione primero una obra");
        }
    }//GEN-LAST:event_watchFunctionsActionPerformed

